def main(matrix,starting,ending):
  global m
  global a
  global zoom
  global start
  global end
  global borders
  global images

  images=[]
  a = matrix
  zoom = 20
  borders = 6
  start = starting
  #start= 1,1
  #end= 5,5
  end = ending

  if a[start[0]][start[1]]==1:
    return "No path"

  if a[end[0]][end[1]]==1:
    return "No path"


  try:
    #create matrix m - all values are assigned 0 and setting up starting point by putting a 1 
    m = []
    for i in range(len(a)):
        m.append([])
        for j in range(len(a[i])):
            m[-1].append(0)
    i,j = start
    m[i][j] = 1

    #put numbers as described above to get path until end is assigned a number
    k = 0
    while m[end[0]][end[1]] == 0:
        k += 1
        make_step(k) 
        draw_matrix(a, m) #pillow image of possible moves

    """
    We now find the shortest Path:
    1)Go to the ending point, say, the number there is k
    2)Find a neighbor cell with a value k-1 , go there, decrease k by one
    3)Repeat the previous step until we get to the starting point, i.e., k=1
    """
    i, j = end
    k = m[i][j]
    the_path = [(i,j)]
    while k > 1:
      if i > 0 and m[i - 1][j] == k-1:
        i, j = i-1, j
        the_path.append((i, j))
        k-=1
      elif j > 0 and m[i][j - 1] == k-1:
        i, j = i, j-1
        the_path.append((i, j))
        k-=1
      elif i < len(m) - 1 and m[i + 1][j] == k-1:
        i, j = i+1, j
        the_path.append((i, j))
        k-=1
      elif j < len(m[i]) - 1 and m[i][j + 1] == k-1:
        i, j = i, j+1
        the_path.append((i, j))
        k -= 1
      draw_matrix(a, m, the_path)

    for i in range(10):
        if i % 2 == 0:
            draw_matrix(a, m, the_path)
        else:
            draw_matrix(a, m)



    images[0].save('maze.gif',
                   save_all=True, append_images=images[1:],
                   optimize=False, duration=1, loop=0)

    return (the_path)

  except:
    return "No path"

