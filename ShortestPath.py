#Everywhere around 1 we put 2 , if there is no wall...so on
#once we put a number at the ending point, we stop. This number is actually the minimal path length

def make_step(k):
  #searches for the step assigned k and then assigns k+1 to the next step in the path 
  """
  We find a number that corresponds to the step number k, look at the surrounding cells, and check if:
  1) There is no number yet
  2) There is no wall
  set k+1 to those cells
  """
